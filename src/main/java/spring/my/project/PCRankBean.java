package spring.my.project;

public class PCRankBean {
	
	private String rank;
	private String name;
	private String game_type;
	private String game_company;
	private String m_share;
	private String link;
	private String image;
	
	
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGame_type() {
		return game_type;
	}
	public void setGame_type(String game_type) {
		this.game_type = game_type;
	}
	public String getGame_company() {
		return game_company;
	}
	public void setGame_company(String game_company) {
		this.game_company = game_company;
	}

	public String getM_share() {
		return m_share;
	}
	public void setM_share(String m_share) {
		this.m_share = m_share;
	}

	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
	
}
